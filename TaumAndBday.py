def taumBday(b, w, bc, wc, z):
    # Write your code here
    # Calculate the cost of buying gifts of the same color
    cost_same_color = b * min(bc, wc + z) + w * min(wc, bc + z)

    # Calculate the cost of buying gifts of different colors and converting
    cost_diff_color = b * bc + w * wc

    # Return the minimum of the two costs
    return min(cost_same_color, cost_diff_color)


