def hurdleRace(k, height):
    # Write your code here
    # Find the maximum height of hurdles
    max_hurdle = max(height)

    # Calculate the difference between the character's jump height and the maximum hurdle height
    doses_needed = max_hurdle - k

    # If the character can already jump all the hurdles, return 0 doses
    if doses_needed <= 0:
        return 0
    else:
        return doses_needed

